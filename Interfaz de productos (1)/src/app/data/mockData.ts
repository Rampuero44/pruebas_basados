// Función para obtener todos los precios de un producto en diferentes comercios
export function getAllPricesForProduct(productId: number): Array<{ precio: Precio; comercio: Comercio }> {
  // En un sistema real, esto vendría de la base de datos
  // Aquí simulamos que algunos productos tienen múltiples precios
  const basePriceId = mockProducts.find(p => p.id === productId)?.idPrecio || 1;
  
  return [
    { precio: precios[basePriceId - 1], comercio: comercios[0] },
    { 
      precio: { ...precios[basePriceId - 1], idPrecio: basePriceId + 100, valor: precios[basePriceId - 1].valor + 500, idComercio: 2 }, 
      comercio: comercios[1] 
    },
    { 
      precio: { ...precios[basePriceId - 1], idPrecio: basePriceId + 200, valor: precios[basePriceId - 1].valor - 300, idComercio: 3 }, 
      comercio: comercios[2] 
    },
  ];
}
